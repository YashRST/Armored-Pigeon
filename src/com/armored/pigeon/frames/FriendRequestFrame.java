    private void acceptRequestBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_acceptRequestBtnActionPerformed
        try{
            Socket socket = APServerConnector.getConnection();
            APServerOPInitializer aps = new APServerOPInitializer();
            APServerPacketSender apSend = new APServerPacketSender(socket);
            //APServerPacketReceiver apReceive = new APServerPacketReceiver(socket);
            APServerUtilities apUtil = new APServerUtilities();
            
            aps.setOperationCode("REQ001");
System.out.println("friend Sent = "+friend.getName());
            aps.setMsgString(friend.getLoginID() + "," + user.getLoginId());
            
            byte[] outArr = apUtil.convertToBytes(aps);
            
            apSend.sendPacketSize(outArr.length);
            apSend.send(outArr);
            
            user.addFriend(friend);
            user.removeRequest(friend);
            
            CacheProcessor writer = new CacheProcessor(user.getLoginId());
            writer.updateCache(user);
            
        }catch(Exception e){
            e.printStackTrace();
        }
        this.setVisible(false);
    }//GEN-LAST:event_acceptRequestBtnActionPerformed

