    private void RejectRequestActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_RejectRequestActionPerformed
        try{
            Socket socket = APServerConnector.getConnection();
            APServerOPInitializer aps = new APServerOPInitializer();
            APServerPacketSender apSend = new APServerPacketSender(socket);
            APServerUtilities apUtil = new APServerUtilities();
            
            aps.setOperationCode("REQ000");
            aps.setMsgString(friend.getLoginID() + "," + user.getLoginId());
            
            byte[] outArr = apUtil.convertToBytes(aps);
            
            apSend.sendPacketSize(outArr.length);
            apSend.send(outArr);
            
            user.removeRequest(friend);
            
        }catch(Exception e){
            e.printStackTrace();
        }
        this.setVisible(false);
    }//GEN-LAST:event_RejectRequestActionPerformed

